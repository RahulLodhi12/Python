kahani = "Once upon a time there was a guy named Pinkesh who upload tech content on youtube"

print(len(kahani)) # It returns the length of the string.

print(kahani.endswith("qwerty")) # It returns True or False, here string(kahani) doesn't end with "qwerty".
print(kahani.endswith("youtube"))

print(kahani.count("u"))        # It counts the total number of occurrences of any character/string.

print(kahani.capitalize()) # This function capitalizes the first character and make rest lowercase character of a given string.

print(kahani.find("upon")) # This function finds a word and returns the index of first occurrence of that word in the string.

print(kahani.replace("Pinkesh","Bido")) # This function replaces the old word with the new word in the entire string.

